# Create a dictionary of ages
ages = {"Alice": 25, "Bob": 30, "Charlie": 35, "David": 40}

# Accessing keys
print("Keys:", ages.keys())

# Accessing values
print("Values:", ages.values())

# Accessing key-value pairs
print("Key-Value Pairs:", ages.items())

# Iterate over the dictionary and print each key-value pair
for key, value in ages.items():
    print(key, ":", value)
